package com.sist.server;

public class InfomClient {
	private String name;
	private String id;
	private String pass;
	private int image;
	private int win;
	private int lose;
	private long winningLate;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public int getImage() {
		return image;
	}
	public void setImage(int image) {
		this.image = image;
	}
	public int getWin() {
		return win;
	}
	public void setWin(int win) {
		this.win = win;
	}
	public int getLose() {
		return lose;
	}
	public void setLose(int lose) {
		this.lose = lose;
	}
	public long getWinningLate() {
		return winningLate;
	}
	public void setWinningLate(long winningLate) {
		this.winningLate = winningLate;
	}
	
	
}
